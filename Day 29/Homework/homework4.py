# 4. შექმენით ფუნქცია სახელად comparison,
#  რომელიც არგუმენტად იღებს 2 რიცხვს და პრინტავს მათ შორის ჩატარებული 4 არითმეტიკული ოპერაციის შედეგს: <,>,<=,>=.

def comparision(num1, num2):
    print(f"მიმატება: {num1 + num2}")
    print(f"გამოკლება: {num1 - num2}")
    print(f"გამრავლება: {num1 * num2}")
    print(f"გაყოფა: {num1 / num2}")


comparision(10, 2)