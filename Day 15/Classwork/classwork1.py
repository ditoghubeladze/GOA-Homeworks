# 1. Create an if-else statement to check if the temperature exceeds 30 degrees.
#  If it does, print "It's hot outside!"; otherwise, print "It's not too hot."


temperature = int(input("Temperature (C): "))

if temperature > 30:
    print("It's hot outside!")
else:
    print("It's not too hot")



